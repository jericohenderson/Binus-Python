# Proses untuk mmenghitungkan volume cuboid dengan operator arimatika

# Input
panjang = float(input("Masukkan panjang kuboid"))
lebar = float(input("Masukkan lebar kuboid"))
tinggi = float(input("Masukkan tinggi kuboid"))

# Rumus menghitung volume kuboid

volume = panjang * lebar * tinggi

# Print hasil
print ("Volume kuboid adalah {volume} satuan kubik")
